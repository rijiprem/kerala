       <p>
        പൊന്മുടിയിലേക്കു ഒരു റോഡു യാത്ര ഏതു സമയത്തും ഉല്ലാസകരമാണ്. നഗരത്തിനടുത്ത് ഇത്ര ശാന്തവും പ്രകൃതിസുന്ദരവുമായ പ്രദേശം മറ്റെങ്ങും ഉണ്ടാകാനിടയില്ല. കടല്‍ തീരത്താണ് തിരുവനന്തപുരം നഗരം. എന്നാല്‍ പൊന്മുടിയിലേക്കുള്ള യാത്ര തുടങ്ങി അരമണിക്കൂര്‍ പിന്നിടേണ്ട, ഉയരം കൂടുന്ന ഭൂപ്രകൃതിയും ചെറുകുന്നുകളും പച്ചപ്പും തണുത്ത കാറ്റും സ്വാഗതം ചെയ്യും. പൊന്മുടിയിലേക്കുള്ള യാത്രയിലുളള ഇടത്താവളമാണ് കല്ലാര്‍. പൊന്മുടിയുടെ മലനിരകളില്‍ നിന്ന് ഉദ്ഭവിക്കുന്ന കല്ലാര്‍ സമതലങ്ങളിലേക്കു പ്രവേശിക്കുന്ന ഇടമാണിത്.

